package oppStandard.Interface;

public class Definition {
    /**
     *声明类的关键字是 class， 声明接口的关键字是 interface
     * 普通类： 只有具体实现
     * 抽象类： 具体实现和规范（抽象方法）都有！
     * 接口： 只有规范，自己无法写方法，专业的约束! 约束和实现的分离：面相接口编程
     *
     * 接口就是规范，定义的一组规则
     * 接口的本质是契约，制定好后大家都遵守
     * OO的精髓，是对对象的抽象，最能体现这一点的就是接口.
     */
    /**
     * 接口的作用
     * 约束
     * 定义一些方法，让不同的人实现
     * 方法都由 public abstract定义
     * 常量 public static final
     * 接口不能被实例化， 接口中没有构造方法
     * implements 可以实现多个接口
     * 必须要重写接口中的方法
     */
}
