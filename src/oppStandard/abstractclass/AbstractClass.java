package oppStandard.abstractclass;

public class AbstractClass {
    /**
     * abstract 修饰符可以用来修饰方法也可用来修饰类，如果修饰方法，那么该方法就是抽象方法，如果修饰类，那么该类就是抽象类
     * 抽象类中可以没有抽象方法，但是有抽象方法的类一定要生明为抽象类
     *
     * 抽象类，不能使用 new关键字来创造对象，它是用来让子类继承的
     * 抽象的方法，只有方法的声明，没有方法的实现，它是用来让子类继承的
     *
     * 子类继承抽象类，那么就必须要实现抽象类没有实现的抽象方法，否则该子类也要声明为抽象类
     * 抽象类中也存在构造器
     * 存在的意义 抽象出来 提高开发效率
     */
}
