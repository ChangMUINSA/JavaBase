package oppStandard;

public class Packaging {
    /**
     * 该露的露，该藏的藏
     * 高内聚，低耦合
     * 高内聚： 类的内部数据操作细节自己完成，不允许外部干涉
     * 低耦合： 今暴露少量的方法给外部使用
     * 封装（数据的隐藏）
     * 通常应该禁止访问一个对象中数据的实际表示，而应该通过操作接口访问，这称为信息隐藏
     * 属性私有， get/set
     */
}
