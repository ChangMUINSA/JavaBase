public class VariableAndScope {

    //属性

    //===============================================================================================
    //类变量 static
    static double salary = 30000;
    //===============================================================================================



    //===============================================================================================
    //实例变量:从属于对象
    // 不需要初始化；但是如果不自行初始化则会变成这个类型的默认值.
    // 数值类型：0/0.0
    // String类型： u000 null
    //布尔类型 ：false
    //除了基本类型其余的默认值都是空
    int age;
    String name;
    //================================================================================================

    //main方法
    public static void main(String[] args){

        //============================================================================================
        //局部变量： 在方法或语句块里的变量；必须声明和初始化值.
        int i = 10;
        System.out.println(i);
        //============================================================================================

        //==============================================================================================
        //实例变量如何使用
        //VariableAndScope variableAndScope = new VariableAndScope();// ALT + ENTER +ENTER,先输入后面的部分
        VariableAndScope example = new VariableAndScope();
        System.out.println(example.age);
        System.out.println(example.name);
        //==============================================================================================


        //==============================================================================================
        //类变量如何使用
        System.out.println(salary);
        //==============================================================================================

    }

    //其他方法
    public void add() {

    }
}
